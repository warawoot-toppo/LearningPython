'''
กำหนดให้ list4 = ['a', 'b', 'c', 'd', 'e', 'f'] เขียนโปรแกรมรับ input 1 ตัวที่เป็น int (nub1)
จากนั้นให้ตรวจสอบว่า num1 มีค่าไม่เกิน index สุดท้ายของ list4 หรือไม่
- ถ้าไม่เกินให้ลบสมาชิกใน index ที่ num1 โดยใช้คำสั่ง del
- ถ้าเกินไม่ดำเนินการใดๆ
'''
list4 = ['a', 'b', 'c', 'd', 'e', 'f']
num1 = int(input('= '))
if num1 <= len(list4) - 1:
    del list4[num1]
print(list4)    
